    private void tablalistaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistaMouseClicked
        // btnguardar cambia el estado a "Editar"
        btnguardar.setText("Editar");
        //llamada a la funcion habiliat que creamos arriba
        habilitar();
        btneliminar.setEnabled(true);
        //decimos que la accion del boton cambia a editar.
        accion="editar";

        int fila =tablalista.rowAtPoint(evt.getPoint());

        txtidreserva.setText(tablalista.getValueAt(fila, 0).toString());
        txtidhabitacion.setText(tablalista.getValueAt(fila, 1).toString());
        txtnumhabitacion.setText(tablalista.getValueAt(fila, 2).toString());
        txtidcliente.setText(tablalista.getValueAt(fila, 3).toString());
        txtcliente.setText(tablalista.getValueAt(fila, 4).toString());
        txtidtrabajador.setText(tablalista.getValueAt(fila, 5).toString());
        txttrabajador.setText(tablalista.getValueAt(fila, 6).toString());
       
        cbotipo_reserva.setSelectedItem(tablalista.getValueAt(fila, 7).toString());
       
        dcfechareserva.setDate(Date.valueOf(tablalista.getValueAt( fila, 8).toString()));
        dcfechaingreso.setDate(Date.valueOf(tablalista.getValueAt(fila, 9).toString()));
        dcfechasalida.setDate(Date.valueOf(tablalista.getValueAt(fila, 10).toString()));
        
        txtprecio_total.setText(tablalista.getValueAt(fila, 11).toString());
        cboestado.setSelectedItem(tablalista.getValueAt(fila, 12).toString());

    }//GEN-LAST:event_tablalistaMouseClicked

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

        //Condicion para comprobar la habitaci√≥n
        if(!txtidreserva.getText().equals("")){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¬øEsta seguro de eliminar la reserva?", "Confirmar",2);

            //condici√≥n para confirmar la eliminaci√≥n del registro en la tabla
            if(confirmacion ==0){
                ReservaControl funcion = new ReservaControl();
                DatosReserva dts = new DatosReserva();

                dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
                funcion.eliminar(dts);
                mostrar("");
                inhabilitar();

            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

