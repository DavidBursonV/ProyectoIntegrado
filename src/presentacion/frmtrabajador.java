    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:

        if(txtnombre.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del empleado.");
            txtnombre.requestFocus();
            return;
        }
        if(txtprimer_apellido.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes escribir el primer apellido del empleado.");
            txtprimer_apellido.requestFocus();
            return;
        }
        if(txtseg_apellido.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar el segundo apellido del empleado.");
            txtseg_apellido.requestFocus();
            return;
        }
        
        if(txtnum_dni.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar el DNI del empleado.");
            txtnum_dni.requestFocus();
            return;
        }
        
        if(txtsueldo.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar un sueldo para el empleado.");
            txtsueldo.requestFocus();
            return;
        }
        
        if(txtlogin.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar un login para el empleado.");
            txtlogin.requestFocus();
            return;
        }
        
        if(txtcontraseña.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar una contraseña para el empleado.");
            txtcontraseña.requestFocus();
            return;
        }

        DatosTrabajador dts=new DatosTrabajador();
        TrabajadorControl funcion = new TrabajadorControl();

        dts.setNombre(txtnombre.getText());
        dts.setPrimer_apellido(txtprimer_apellido.getText());
        dts.setSeg_apellido(txtseg_apellido.getText());
        int seleccion=cbotipo_documento.getSelectedIndex();
        dts.setTipo_documneto((String)cbotipo_documento.getItemAt(seleccion));
        dts.setNum_dni(txtnum_dni.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setSueldo(Double.parseDouble(txtsueldo.getText()));
        seleccion=cboacceso.getSelectedIndex();
        dts.setAcceso((String)cboacceso.getItemAt(seleccion));
        dts.setLogin(txtlogin.getText());
        dts.setPassword(txtcontraseña.getText());
        seleccion=cboestado.getSelectedIndex();
        dts.setEstado((String)cboestado.getItemAt(seleccion));
        

        //Método para guardar un nuevo registro
        if(accion.equals("guardar")){
            if(funcion.insertar(dts) ){
                JOptionPane.showMessageDialog(rootPane, "El empleado se ha registrado con exito");
                mostrar("");
                inhabilitar();
            }
        }

        else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            //Condicional para editar un registro
            if(funcion.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El empleado se ha editado con exito");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

