    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        try {
            DefaultTableModel modelo;
            TrabajadorControl funcion = new TrabajadorControl();
            DatosTrabajador dts = new DatosTrabajador();
            
            
            dts.setLogin(txtusuario.getText());
            dts.setPassword(txtpassword.getText());
            
            modelo = funcion.login(dts.getLogin(),dts.getPassword() );
            
            tablalistado.setModel(modelo);
            
            if (funcion.totalregistros >0) {
                this.dispose();
                frminicio form = new frminicio();
                form.toFront();
                form.setVisible(true);
                frminicio.lblidpersona.setText(tablalistado.getValueAt(0,0).toString());
                frminicio.lblnombre.setText(tablalistado.getValueAt(0,1).toString());
                frminicio.lblprimerapellido.setText(tablalistado.getValueAt(0,02).toString());
                frminicio.lblsegapelldio.setText(tablalistado.getValueAt(0,3).toString());
                frminicio.lblacceso.setText(tablalistado.getValueAt(0,4).toString());
                
                if (!frminicio.lblacceso.getText().equals("Admin.")) {
                    
                    frminicio.menuarchivo.setEnabled(false);
                    frminicio.menuconficuracion.setEnabled(false);
                }
                
            }else{
                JOptionPane.showMessageDialog(rootPane,"Acceso invalido", "Acceso al sistema ", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btningresarActionPerformed

