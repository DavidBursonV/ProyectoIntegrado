    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:

        if(txtnombre.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del cliente.");
            txtnombre.requestFocus();
            return;
        }
        if(txtprimer_apellido.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes escribir el primer apellido.");
            txtprimer_apellido.requestFocus();
            return;
        }
        if(txtseg_apellido.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar el segundo apellido.");
            txtseg_apellido.requestFocus();
            return;
        }
        
        if(txtnum_dni.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar el DNI del cliente.");
            txtnum_dni.requestFocus();
            return;
        }
        
        if(txtcodigo_cliente.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes insertar un cÃ³digo para el cliente.");
            txtcodigo_cliente.requestFocus();
            return;
        }

        DatosCliente dts=new DatosCliente();
        ClienteControl funcion = new ClienteControl();

        dts.setNombre(txtnombre.getText());

        dts.setPrimer_apellido(txtprimer_apellido.getText());

        dts.setSeg_apellido(txtseg_apellido.getText());

        int seleccion=cbotipo_documento.getSelectedIndex();
        dts.setTipo_documneto((String)cbotipo_documento.getItemAt(seleccion));
        dts.setNum_dni(txtnum_dni.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setCodigo_cliente(txtcodigo_cliente.getText());

        //MÃ©todo para guardar un nuevo registro
        if(accion.equals("guardar")){
            if(funcion.insertar(dts) ){
                JOptionPane.showMessageDialog(rootPane, "El cliente se ha registrado con exito");
                mostrar("");
                inhabilitar();
            }
        }

        else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            //Condicional para editar un registro
            if(funcion.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El cliente se ha editado con exito");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

