    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

        //Condicion para comprobar la habitaciÃ³n
        if(!txtidpersona.getText().equals("")){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Â¿Esta seguro de eliminar este cliente?", "Confirmar",2);

            //condiciÃ³n para confirmar la eliminaciÃ³n del registro en la tabla
            if(confirmacion ==0){
                ClienteControl funcion = new ClienteControl();
                DatosCliente dts = new DatosCliente();

                dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));
                funcion.eliminar(dts);
                mostrar("");
                inhabilitar();

            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

